/**
 * Get a translation string with optional parameter substitution.
 * Usage: t('policy.daysRemaining', { n: 3 }) → "3 of 7 days remaining"
 */
export function getTranslator(lang: string) {
  const code = (lang in translations ? lang : 'en') as LangCode;
  const dict = translations[code];
  const en = translations.en;

  return function t(key: string, params?: Record<string, string | number>): string {
    let str = dict[key] ?? en[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
      }
    }
    return str;
  };
}
